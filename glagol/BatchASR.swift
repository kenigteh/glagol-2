import Foundation

/// Port (абстракция) для batch ASR-движка.
///
/// **Batch-режим:** аудио записывается полностью на стороне `AudioRecorder`,
/// затем целиком передаётся в `transcribe(audio:)`. Никакого стриминга,
/// никакой инкрементальной отдачи partials. Получили запись → отдали в модель
/// → получили готовый текст одним блоком.
///
/// Этот подход чище для on-device диктовки на современных моделях:
///   - Нет race conditions, hallucination'а на silence, word-tail leakage'а
///     при cut'ах и прочих болезней streaming-режима.
///   - Модель видит ПОЛНЫЙ контекст → пунктуация и интонация глобально
///     консистентные.
///   - На 22-секундной фразе Qwen3-ASR-1.7B-4bit отдаёт результат за 0.6с
///     (warm), что почти неощутимо после нажатия stop.
///
/// Все callbacks вызываются на main thread.
///
/// Текущая реализация — `QwenASR` (Qwen3-ASR через speech-swift, MLX на Apple Silicon).
protocol BatchASR: AnyObject {
    /// Ошибка от движка (загрузка модели, runtime, и т.п.).
    var onError: ((String) -> Void)? { get set }

    /// Меняется когда движок готов / неготов принимать запросы (например,
    /// загрузка модели завершилась).
    var onReadyChange: ((Bool) -> Void)? { get set }

    /// Статус долгих операций (загрузка модели, переключение, транскрипция).
    /// Опционально — адаптер может не присылать ничего; UI должен это терпеть.
    var onStatus: ((String) -> Void)? { get set }

    /// Готов ли движок принять `transcribe(audio:)`.
    var isReady: Bool { get }

    /// Поднять движок (загрузить модель, поднять runtime). Вызывается один раз
    /// при старте приложения. Реализация может работать асинхронно —
    /// `isReady` станет true когда модель прогрелась.
    func warmUp()

    /// Перечитать внешнюю конфигурацию (словарь хотвордов и т.п.).
    /// Адаптерам без mutable-конфига дефолтная реализация даёт no-op.
    func reload()

    /// Транскрибировать полную запись.
    /// Аудио: 16 kHz mono Float32 в диапазоне [-1, 1].
    /// Возвращает финальный текст (пустая строка если ничего не распознано).
    func transcribe(audio: [Float]) async throws -> String

    /// Закрыть движок (вызывается при выходе приложения).
    func shutdown()
}

extension BatchASR {
    /// Дефолт для адаптеров без mutable-конфига.
    func reload() {}
}
